CREATE TABLE IF NOT EXISTS PRODUCTO (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PRECIO NUMBER(10, 2) NOT NULL,
    STOCK NUMBER(10) NOT NULL,
    DESCRIPCION VARCHAR2(255) NOT NULL,
    NOMBRE VARCHAR2(100) NOT NULL
);

CREATE TABLE IF NOT EXISTS CLIENTE (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(100) NOT NULL,
    NRO_DOCUMENTO VARCHAR2(20) NOT NULL,
    EMAIL VARCHAR2(100) NOT NULL,
    PHONE VARCHAR2(20) NOT NULL,
    ADDRESS VARCHAR2(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS COMPRA (
    COMPRA_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CLIENTE_ID NUMBER NOT NULL,
    FOREIGN KEY (CLIENTE_ID) REFERENCES CLIENTE(ID)
);

CREATE TABLE IF NOT EXISTS COMPRA_PRODUCTO (
    COMPRA_ID NUMBER NOT NULL,
    PRODUCTO_ID NUMBER NOT NULL,
    PRIMARY KEY (COMPRA_ID, PRODUCTO_ID),
    FOREIGN KEY (COMPRA_ID) REFERENCES COMPRA(COMPRA_ID),
    FOREIGN KEY (PRODUCTO_ID) REFERENCES PRODUCTO(ID)
);

CREATE TABLE IF NOT EXISTS PAGO (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MEDIO_PAGO VARCHAR2(50) NOT NULL,
    TOTAL_PAGO NUMBER(10, 2) NOT NULL,
    FECHA_PAGO DATE NOT NULL,
    COMPRA_ID NUMBER NOT NULL,
    FOREIGN KEY (COMPRA_ID) REFERENCES COMPRA(COMPRA_ID)
);

CREATE TABLE IF NOT EXISTS ORDEN_COMPRA (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FECHA_ORDEN DATE NOT NULL,
    PAGO_ID NUMBER NOT NULL,
    FOREIGN KEY (PAGO_ID) REFERENCES PAGO(ID)
);

